class RewardControllerTest extends TestCase
{
    // use DatabaseMigrations;
    use RefreshDatabase;

    /**
     * A basic feature test example.
     *
     * @return void
     */
    public function test_reward_can_be_created()
    {
        // $this->seed(UsersTableSeeder::class);
        // $this->seed(RolesSeeder::class);
        // $this->seed(RoleUserSeeder::class);
        
        Storage::fake('file');

        $file = UploadedFile::fake()->image('test.jpg');
        
        $rewardData = [
            'reward' => 'Test Reward',
            'pointvalue' => 10,
            'file' => $file
        ];

        // User is generated by the seeder
        $user = User::find(1);
        
        $response = $this->actingAs($user)->postJson('/api/rewards', $rewardData);

        $response->assertStatus(200);
    }

    public function test_rewards_collection_can_be_fetched()
    {
        // $this->seed(UsersTableSeeder::class);
        // $this->seed(RolesSeeder::class);
        // $this->seed(RoleUserSeeder::class);
        // $this->seed(rewards_table_seeder::class);

        $user = User::find(1);

        $response = $this->actingAs($user)->getJson('/api/rewards');

        $response->assertStatus(200);

        $response->assertJsonCount(2);
    }

    public function test_reward_can_be_updated()
    {
        // $this->seed(UsersTableSeeder::class);
        // $this->seed(RolesSeeder::class);
        // $this->seed(RoleUserSeeder::class);
        // $this->seed(rewards_table_seeder::class);
        
        Storage::fake('file');

        $file = UploadedFile::fake()->image('testModified.jpg');

        $rewardData = Reward::find(1);
        
        $rewardData->reward = $rewardData->reward . " Updated";
        $rewardData->pointvalue = 100;
        $rewardData->file = $file;

        // User is generated by the seeder
        $user = User::find(1);
        
        $response = $this->actingAs($user)->putJson('/api/rewards/' . $rewardData->id, $rewardData->toArray());

        $response->assertStatus(200);
    }

    public function test_a_reward_can_be_deleted()
    {
        // $this->seed(UsersTableSeeder::class);
        // $this->seed(RolesSeeder::class);
        // $this->seed(RoleUserSeeder::class);
        // $this->seed(rewards_table_seeder::class);

        $user = User::find(1);

        $preDeleteFetchResponse = $this->actingAs($user)->getJson('/api/rewards');

        $preDeleteFetchResponse->assertStatus(200);

        $preDeleteFetchResponse->assertJsonCount(2);

        $reward = Reward::find(1);

        $deleteResponse = $this->actingAs($user)->deleteJson('/api/rewards/' . $reward->id);

        $deleteResponse->assertStatus(200);

        $postDeleteFetchResponse = $this->actingAs($user)->getJson('/api/rewards');

        $postDeleteFetchResponse->assertStatus(200);

        $postDeleteFetchResponse->assertJsonCount(1);
    }
}
